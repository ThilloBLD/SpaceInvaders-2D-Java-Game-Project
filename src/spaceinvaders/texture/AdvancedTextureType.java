package spaceinvaders.texture;

// Enumeration, die verschiedene Arten von Texturen repräsentiert
public enum AdvancedTextureType {
    // Textur für den Spieler
    player,
    // Textur für den Gegner
    enemy,
    // Textur für die Spielerkugel
    bullet,
    // Textur für die umgekehrte Spielerkugel
    reversedbullet;
}